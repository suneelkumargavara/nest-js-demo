import { Expose, Transform } from 'class-transformer';
import { UserDTO } from '../../users/dtos/user.dto';

export class ReportDTO {
  @Expose()
  id: string;

  @Expose()
  approved: boolean;

  @Expose()
  price: number;

  @Expose()
  year: number;

  @Expose()
  lng: number;

  @Expose()
  lat: number;

  @Expose()
  make: string;

  @Expose()
  model: string;

  @Expose()
  mileage: number;

  @Transform(({ obj }) => obj.user.id)
  @Expose()
  userId: number;
}
